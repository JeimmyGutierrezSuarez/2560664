def suma (a,b):   #se define una función la cual es suma y sus parametros son a y b   
    return a+b     #retorna la suma de los parametros a y b

def suma(a,b,c):    #se define una función la cual es suma y sus parametros son a,b y c
    return a+b+c    #retorna la suma de los parametros a,b y c

print(suma(1,2,3))     #imprime la duncion llamada la cual es suma y aqui se definen sus parametros los cuales son 1,2 y 3, esta toma como valor la ultima funcion creada y hace la  suma
